CREATE TABLE 'REVIEW_LOG' (
    'TYPE' VARCHAR(50),
    'ACTION' VARCHAR(10),
    'REVIEW_ID' VARCHAR(500),
    'CONTENT' VARCHAR(4000),
    'ATTACHED_PHOTO_IDS' VARCHAR(4000),
    'USER_ID' VARCHAR(500),
    'PLACE_ID' VARCHAR(500)
)CHARSET=utf8
;

CREATE TABLE 'REVIEW' (
    'REVIEW_ID' VARCHAR(500),
    'USER_ID' VARCHAR(500),
    'PLACE_ID' VARCHAR(500),
    'CONTENT' VARCHAR(4000),
    'ATTACHED_PHOTO_IDS' VARCHAR(4000),
    PRIMARY KEY('REVIEW_ID', 'USER_ID', 'PLACE_ID'),
    UNIQUE INDEX UIDX_REVIEW('REVIEW_ID', 'USER_ID', 'PLACE_ID')
)CHARSET=utf8
;

CREATE TABLE 'REVIEW_SCORE' (
    'USER_ID' VARCHAR(500),
    'REVIEW_ID' VARCHAR(500),
    'SCORE' INT,
    PRIMARY KEY('USER_ID', 'REVIEW_ID'),
    FOREIGN KEY('REVIEW_ID') REFERENCES REVIEW('REVIEW_ID') ON DELETE CASCADE
    UNIQUE INDEX UIDX_REVIEW('USER_ID', 'REVIEW_ID')
)CHARSET=utf8
;